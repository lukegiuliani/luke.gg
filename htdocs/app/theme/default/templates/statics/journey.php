	<div class="constrain">
		<h3>The <em>&lsquo;is this even possible?&rsquo;</em> stage</h3>
		<p>Some clients are&rsquo;t entirely sure what actual problem they are trying to tackle, or if digital is the right tool for the job.</p>
		<p>Often, people leap ahead:</p>
		<blockquote class="blue">
			<q>&ldquo;How much does it cost, and how long will it take?&rdquo;</q>
		</blockquote>
		
		<p>This question is a real piece-of-string-length question. Better questions to ask are like the following:</p>
		
		<dl>
			<dt>Q:</dt><dd><strong>Are we creating a behaviour change?</strong> If so, what are we changing? What is the specific audience?</dd>
			<dt>Q:</dt><dd><strong>How will we measure success?</strong> In numbers. Either an increase or a creation. Specificity rocks.</dd>
			<dt>Q:</dt><dd><strong>What is the end impact?</strong> Less call centre traffic? More revenue? Less friction within departments?</dd>
		</dl>
		
		<p>Try to define discreet outcomes. Big statements like:</p>
		<p class="smallquote"><q><em>&lsquo;Make the brand awesome!&rsquo;</em></q></p>
		<p>... aren&rsquo;t as useful as something more specific:</p>
		<p class="smallquote"><q><em>&lsquo;Get 50% more traffic, and 10% more sales of product X.&rsquo;</em></q></p>
		<p><strong>Remember, outcomes trump mechanisms.</strong> Getting lost in &lsquo;how&rsquo; isn&rsquo;t useful. In the above example, the re-branding is just one <em>mechanism</em> to help deliver the <em>outcome</em>.</p>
		<hr>
				
		<h3>Warning bells:</h3>
		<p>Some common warnings of bad assumptions that should be challenged at the ideation stage before moving on:</p>
		<dl>
			<dt><i class="ss-gizmo ss-alert"></i></dt><dd><strong>Constraining tech.</strong> The oft-uttered <em>&lsquo;Do it in Wordpress!&rsquo;</em> approach. Common culprits include Drupal and Salesforce. People know they exist, but putting that cart before the horse leads to mess.</dd>
			<dt><i class="ss-gizmo ss-alert"></i></dt><dd><strong>Full functionality at launch.</strong> Because if tons of functionality works for Facebook, it&rsquo;ll work for us, right? Probably not. And remember, Facebook first looked like <a href="<?php ET::image('journey/2005-facebook.jpg'); ?>" target="_blank">this</a> back in 2005:</dd>
			<dt><i class="ss-gizmo ss-alert"></i></dt><dd><strong>Do it yesterday!</strong> There is a nasty assumption that just because someones nephew once made a blog in 5 minutes, all internet stuff is fast. It takes anywhere from 1 to 3 months to build anything of useful value.</dd>
		</dl>
		
		<p>For more examples of common mistakes, check out <a>Five digital innovation myths, busted</a> (PDF, 10 pages).</p>
		<hr>
		
		<h3>Workshopping discovery</h3>
		<p>If a client is struggling, these things can help:</p>
		<ol>
			<li>An hour or so of scribbling over coffee with a member of our team. We do this for free, because sharing is caring.</li>
			<li>A structured half-day immersion with our team, at our studio. We whiteboard out the problem and create a coherent <a href="<?php EF::dest('') ?>agile">Agile</a> roadmap for the build. This is charged out at usual rates.</li>
			<li>The client creates a brief. Good briefs define only desired outcomes and constraints. It&rsquo;s rare to get a good brief. If the client knew how to write a brilliant brief, they wouldn&rsquo;t need our help.</li>
		</ol>
	
