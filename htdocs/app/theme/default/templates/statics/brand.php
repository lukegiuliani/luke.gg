		<h2>Language</h2>
		<h3 class="subhead">Because language is design.</h3>
		<p>Our language set is all about clarity, space and insight. Some things to keep in mind to extend the &lsquo;voice&rsquo; correctly:</p>
		<dl>
			<dt>1</dt><dd>Use lists. They&rsquo;re easy to parse and build structure.</dd>
			<dt>2</dt><dd>Wit. Be funny. Don&rsquo;t try too hard, but remember humans read, and humans are hilarious.</dd>
			<dt>3</dt><dd>No jargon. The user may be <a href="https://www.youtube.com/watch?v=r2CbbBLVaPk" target="_blank">drunk</a>, but they&rsquo;re not an idiot and they don&rsquo;t have attention to waste.</dd>
			<dt>4</dt><dd>Keep slim. Re-read that paragraph you just wrote, get your literary hatchet out and get rid of all the <strike>unnecessarily verbose</strike> long bits.</dd>			
			<dt>5</dt><dd>Punctuate properly. Use <code>&amp;rsquo;</code> for a freakin <code>&rsquo;</code> not a <code>'</code>, or founder Dayble will fiercely kick your arse.</dd>
			<dt>6</dt><dd>Metaphor and storytelling can provide uplift, but they makes for leaky lifeboats. Facts first.</dd>			
		</dl>
		<p>If you&rsquo;re ever in doubt about language, steal the smart goodness from <a href="http://alistapart.com/about/style-guide">A List Apart&rsquo;s style guide</a>. Only real difference is we use Australian English.
