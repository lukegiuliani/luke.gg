	<div class="constrain">
		<h3>The company structure is similar to any professional services firm.</h3>
		<p>That is to say we employ a collection of clever people.</p>
		<p>Other companies hire us as a group to solve problems for them. These are mostly digital in nature, but extend through to strategy, organisational culture and training.</p>
		<p>The overall unit format looks like this:</p>
		<p class="img"><img src="<?php ET::image('organic/delivery-sales-ops.png'); ?>" alt="3 bits"></p>
		<dl>
			<dt>15%</dt><dd><strong>Operations</strong> makes everything run. Accounts, management, HR, culture maintenance and so on.</dd>
			<dt>15%</dt><dd><strong>Sales</strong> finds us the right kinds of clients and makes us look nice to the world.</dd>
			<dt>70%</dt><dd><strong>Delivery</strong> do all the billable stuff. Code and project management and BA / PM type stuff.</dd>
		</dl>	
		<p>The above is an operational roles guideline. For example, at a size of 3 people, everyone would do a bit of everything. At 25 we have individual people doing accounts, sales, project management, etc.</p>
		<p>At the 25 people break-point another similar organism would be birthed, working to the same rules, and scaled.</p>
		<p>Reporting within these three areas should be two-directional and discreet. Think of it like APIs between good self contained systems. The right amount of comms, at the right time, for the right reason.</p>
		<p class="behaviour"><small>Behaviour example</small>The delivery / ops teams need to give constant feedback to sales. Having someone from those teams in weekly sales sprint meetings keeps an open dialogue.</p>
		<hr>
		<h3>Typical roles</h3>
		<p>Importantly, these are <em>roles</em>, not people. Certain talented folks may do multiple roles, or move between them.</p>
		<ul>
			<li><strong>Operations:</strong>
				<ul>
					<li><strong>General Manager:</strong> Responsible for the health, happiness and productivity of the delivery and sales teams. The only person directly responsible to the board.</li>
					<li><strong>Office Assistant:</strong> Keeps the troops fed and watered. Helps people keep the space clean, keeps us stocked in beer and healthy food.</li>
					<li><strong>Accounts:</strong> Makes sure clients pay their bills and employees get paid.</li>
				</ul>
			</li>
			<li><strong>Sales:</strong>
				<ul>
					<li><strong>Matchmaker:</strong> Friendly, front-food people who understand the kinds of people / organisations we work with best. Good at making friends with these kinds of people.</li>
					<li><strong>BDM:</strong> More traditionally &lsquo;business&rsquo; oriented sales folks. Lots of tenders, RFQs and classic B2B sales work.</li>
					<li><strong>Brand manager:</strong> Somewhere between marketing, advertising and lean customer development. Making collateral, outreach material and stuff that shows off how awesome we are.</li>
					<li><strong>Strategist:</strong> Defines market approach, targets, cost of acquisition and other overarching goals within which the sales team operates.</li>
				</ul>
			</li>
			<li><strong>Delivery:</strong>
				<ul>
					<li><strong>Application Developer:</strong> Back-end centric problem solving programming role.</li>
					<li><strong>Front-end dev:</strong> Heavily technical top-of-the-stack centric dev, with a focus on UX architecture, page load times, accessibility and device compatibility.</li>
					<li><strong>Designer:</strong> Visual and UX design, branding, all the way through to language, customer development and user flow stuff.</li>								<li><strong>Agile Iteration Manager:</strong> Essentially a classic PM role, but with strengths in Agile sprints. Reporting to and management of clients and staff, resourcing, etc.</li>
					<li><strong>Business Intelligence:</strong> Process-oriented analysis folks who work almost exclusively with clients on the nuts and bolts of their business function, with a view to translate that into a digital framework.</li>
				</ul>
			</li>
		</ul>
		<p>The above are examples to illustrate our breadth, not categorical roles that must exist for us to function.</p>
