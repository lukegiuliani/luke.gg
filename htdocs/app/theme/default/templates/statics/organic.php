	<div class="constrain">
		<h3>&lsquo;How&rsquo; is often a question about <em>when</em>.</h3>
		<p>The daily / weekly / monthly organisational rhythms sets the heartbeat for motivation, clarity and progress.</p>
		<p>The aim is to keep things as snappy and crisp, even if the actual actual things done change as we evolve.</p>
		<hr>
		<h3>Some example rhythms (as of Aug 2014):</h3>
		<ul>
			<li>Every day at 12:50pm, teams should down tools and do a 5 minute round-table, focussing on what&rsquo;s blocking people from working well that afternoon.</li>
			<li>Lunch happens at 1pm every day, together, with free food and no shop talk allowed.</li>
			<li>Friday at 4pm we all down tools, have a beer and spend an hour and a half talking about what was good, bad and needs change in the week. Focus on showcasing achievements, and resolving tensions.</li>
		</ul>
		<p class="behaviour"><small>Behaviour example</small>Just before 4pm on Friday, someone goes and buys a bunch of beer, then runs around telling everyone to stop working so we can have friday catchup.</p>
		<hr>
		<p><strong>Importantly,</strong> the above is a starting position, not an end point. Keep questioning, stay kind, and stay foolish.</p>
