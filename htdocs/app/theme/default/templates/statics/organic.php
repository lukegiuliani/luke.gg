	<div class="constrain">
		<h3>How is often more important than what.</h3>
		<p>From the <a href="<?php EF::dest('') ?>truth">base truth</a> that we will be solving new problems with new technology every year, <em>how</em> we do stuff is often more important than what tools we use on any given day.</p>
		<hr>
		<h3>Finding truth in what we do.</h3>
		<p>We build upon Simon Sinek&rsquo;s golden circle idea, i.e. that you have to know <em>who you are</em> and <em>how you do stuff</em> before you actually build anything. (See <a href="https://www.youtube.com/watch?v=l5Tw0PGcyN0" target="_blank">the video</a>.)</p>
		<p class="img">
			<img src="<?php ET::image('organic/golden-circle.png'); ?>" alt="image">
		</p>
		<p class="behaviour"><small>Behaviour example</small>If ever someone&rsquo;s stuck on a problem without an obvious answer,  ask <a href="<?php EF::dest('') ?>truth#principles">&ldquo;Is why I&rsquo;m doing helping people, growth and constantly bettering?&rdquo;</a> as per first principles.</p>
		<hr>
		<h3>Congruence</h3>
		<p>If we extend this idea to include our clients, we find that everyone does better work if we&rsquo;re all on the same page.</p>
		<p class="img">
			<img src="<?php ET::image('organic/congruence.png'); ?>" alt="image">
		</p>		

		<p class="behaviour"><small>Behaviour example</small>Client matchmakers / sales staff should ask <strong>why</strong> a client wants to do digital, before asking about functional specifications of any app.</p>
		<h3>Example: The &lsquo;First Date&rsquo; project</h3>
		<p>The first project with any new client should be as small, self contained and clearly measurable as possible.</p>
		<p>Building stuff together with the <em>explicit aim to learn</em> is the fastest way to get our heads around the relationship, figure out the best way to work together.</p>
		<hr>
		<h3>Trust trumps all</h3>
		<p>In looking over our best and worst client relationships, we&rsquo;ve learn that trust is vital. Most screw-ups in digital happen when there&rsquo;s a breakdown of trust between the vendor and client.</p>
		<p class="behaviour"><small>Behaviour example</small>Calling clients every few weeks to gauge <a href="http://en.wikipedia.org/wiki/Net_Promoter" target="_blank">Net promoter scores</a>, whilst not an exact metric, opens up the conversation to constantly better the relationship.</p>
		
		<p>Accordingly, processes should be created around building and maintaining trust, within and without the company.</p>
		<p class="behaviour"><small>Behaviour example</small>Having clients eat lunch at our round-table daily lunches breaks everyone out of their work-brain thinking and help us connect as human beings.</p>
		
